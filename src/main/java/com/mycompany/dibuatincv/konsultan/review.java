    private void btn_sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sendActionPerformed
        // TODO add your handling code here:
String catatan = text_note.getText();
    
    try {
        java.sql.Connection conn = com.mycompany.dibuatincv.DBConnection.getKoneksi();
        
        // UPDATE langsung menuju kolom note_revisi
        String sql = "UPDATE data_diri SET note_revisi = ?, generate = 2 WHERE id_data = ?";
        java.sql.PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, catatan);
        ps.setInt(2, this.idDataTerpilih);
        
        ps.executeUpdate();
        javax.swing.JOptionPane.showMessageDialog(this, "Catatan revisi berhasil dikirim ke Admin!");
        
    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal mengirim catatan: " + e.getMessage());
    }
    konsultan review = new konsultan();
    review.setVisible(true);
    dispose();
    }//GEN-LAST:event_btn_sendActionPerformed

