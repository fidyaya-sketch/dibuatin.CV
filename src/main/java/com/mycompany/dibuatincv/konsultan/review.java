    private void loadDataCV() {
    try {
        java.sql.Connection conn = com.mycompany.dibuatincv.DBConnection.getKoneksi();
        
        // Mengambil kolom path_cv dan nama untuk memuat dokumen visual
        String sql = "SELECT nama, path_cv FROM data_diri WHERE id_data = ?";
        java.sql.PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, this.idDataTerpilih);
        java.sql.ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            String namaFileCV = rs.getString("path_cv");
            
            if (namaFileCV != null && !namaFileCV.isEmpty()) {
                // Lokasi folder tempat aplikasi kamu menyimpan hasil generate gambar CV
                // Sesuaikan "src/output_cv/" dengan lokasi folder penyimpanan asli di project-mu
                String pathLengkap = System.getProperty("user.home") + "/Documents/" + namaFileCV; 
                
                java.io.File fileGambar = new java.io.File(pathLengkap);
                if (fileGambar.exists()) {
    if (namaFileCV.endsWith(".pdf")) {
        // Render halaman pertama PDF jadi gambar
        org.apache.pdfbox.pdmodel.PDDocument doc = 
            org.apache.pdfbox.pdmodel.PDDocument.load(new java.io.File(pathLengkap));
        
        org.apache.pdfbox.rendering.PDFRenderer renderer = 
            new org.apache.pdfbox.rendering.PDFRenderer(doc);
        
        java.awt.image.BufferedImage bim = renderer.renderImageWithDPI(0, 150);
        doc.close();
        
        int targetW = lbl_gambar_cv.getWidth() > 0 ? lbl_gambar_cv.getWidth() : 314;
        int targetH = lbl_gambar_cv.getHeight() > 0 ? lbl_gambar_cv.getHeight() : 410;
        
        java.awt.Image imgSkala = bim.getScaledInstance(targetW, targetH, java.awt.Image.SCALE_SMOOTH);
        lbl_gambar_cv.setIcon(new javax.swing.ImageIcon(imgSkala));
        
    } else {
        // File gambar biasa (PNG, JPG, dll)
        javax.swing.ImageIcon iconAwal = new javax.swing.ImageIcon(pathLengkap);
        
        int targetW = lbl_gambar_cv.getWidth() > 0 ? lbl_gambar_cv.getWidth() : 314;
        int targetH = lbl_gambar_cv.getHeight() > 0 ? lbl_gambar_cv.getHeight() : 410;
        
        java.awt.Image imgSkala = iconAwal.getImage().getScaledInstance(
            targetW, targetH, java.awt.Image.SCALE_SMOOTH
        );
        lbl_gambar_cv.setIcon(new javax.swing.ImageIcon(imgSkala));
    }
} else {
    lbl_gambar_cv.setText("File gambar CV tidak ditemukan di folder.");
}
            } else {
                lbl_gambar_cv.setText("CV Pengguna belum di-generate oleh sistem.");
            }
        }
    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal memuat visual CV: " + e.getMessage());
    }
}
